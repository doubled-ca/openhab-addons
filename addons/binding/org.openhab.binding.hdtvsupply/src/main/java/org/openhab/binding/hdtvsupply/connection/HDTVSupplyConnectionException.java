package org.openhab.binding.hdtvsupply.connection;

public class HDTVSupplyConnectionException extends RuntimeException {

    /**
     *
     */
    private static final long serialVersionUID = -2550264258274309458L;

    public HDTVSupplyConnectionException() {
        super();
    }

    public HDTVSupplyConnectionException(String message) {
        super(message);
    }

    public HDTVSupplyConnectionException(String message, Throwable cause) {
        super(message, cause);
    }

    public HDTVSupplyConnectionException(Throwable cause) {
        super(cause);
    }

}
